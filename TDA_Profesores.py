def crearProfesor(nombre, materia, curso, division):
    return {"nombre": nombre, "materia": materia, "curso": curso, "division": division}


def getNombre(inscripciones):
    inscripciones["nombre"]


def modNombre(inscripciones, nombre):
    inscripciones["nombre"] = nombre


def getMateria(inscripciones):
    inscripciones["materia"]


def modMateria(inscripciones, materia):
    inscripciones["materia"] = materia


def getCurso(inscripciones):
    inscripciones["curso"]


def modCurso(inscripciones, curso):
    inscripciones["curso"] = curso


def getDivision(inscripciones):
    inscripciones["division"]


def modDivision(inscripciones, division):
    inscripciones["division"] = division
